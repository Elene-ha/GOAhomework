
მათემატიკა = float(input("შეიყვანეთ მათემატიკის ქულა: "))
ინგლისური = float(input("შეიყვანეთ ინგლისურის ქულა: "))
ფიზიკა = float(input("შეიყვანეთ ფიზიკის ქულა: "))
ბიოლოგია = float(input("შეიყვანეთ ბიოლოგიის ქულა: "))
გეოგრაფია = float(input("შეიყვანეთ გეოგრაფიის ქულა: "))

# ქულების სიაში შენახვა
score = [მათემატიკა, ინგლისური, ფიზიკა, ბიოლოგია, გეოგრაფია]


sum = 0
for score in score:
    sum += score


Medium = sum / 5


print("მოსწავლის სემესტრის საშუალო ქულა:", Medium)
